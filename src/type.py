        if len(defense_types) == 1:
            defense_index = self.types.index(defense_types[0])
            return self.table[attack_index][defense_index]
        else:
            defense_index0 = self.types.index(defense_types[0])
            effectiveness0 = self.table[attack_index][defense_index0]
            defense_index1 = self.types.index(defense_types[1])
            effectiveness1 = self.table[attack_index][defense_index1]
            return effectiveness0 * effectiveness1

